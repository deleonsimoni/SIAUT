package br.com.siaut.rs.requisicao.dispositivos;

public class CadastrarComodoRequisicao {

	private String token;
	private Integer nuImovel;
	private Integer nuCorPainel;
	private Integer nuIconeImovel;
	private Integer nuComodo;
	private String nomeComodo;
	private String imagemComodo;
	public Integer getNuComodo() {
		return nuComodo;
	}
	public void setNuComodo(Integer nuComodo) {
		this.nuComodo = nuComodo;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public Integer getNuImovel() {
		return nuImovel;
	}
	public void setNuImovel(Integer nuImovel) {
		this.nuImovel = nuImovel;
	}
	public Integer getNuCorPainel() {
		return nuCorPainel;
	}
	public void setNuCorPainel(Integer nuCorPainel) {
		this.nuCorPainel = nuCorPainel;
	}
	public Integer getNuIconeImovel() {
		return nuIconeImovel;
	}
	public void setNuIconeImovel(Integer nuIconeImovel) {
		this.nuIconeImovel = nuIconeImovel;
	}
	public String getNomeComodo() {
		return nomeComodo;
	}
	public void setNomeComodo(String nomeComodo) {
		this.nomeComodo = nomeComodo;
	}
	public String getImagemComodo() {
		return imagemComodo;
	}
	public void setImagemComodo(String imagemComodo) {
		this.imagemComodo = imagemComodo;
	}
}
