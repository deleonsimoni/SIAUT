package br.com.siaut.rs.entity.sensores;

public class SensoresEntity {

	private Long   lngNuId;
	private String strNuTemperatura;
	private String strNuUmidade;
	private String strNuChuva;
	private String strNuLuz;
	
	
	public Long getLngNuId() {
		return lngNuId;
	}
	public void setLngNuId(Long lngNuId) {
		this.lngNuId = lngNuId;
	}
	public String getStrNuTemperatura() {
		return strNuTemperatura;
	}
	public void setStrNuTemperatura(String strNuTemperatura) {
		this.strNuTemperatura = strNuTemperatura;
	}
	public String getStrNuUmidade() {
		return strNuUmidade;
	}
	public void setStrNuUmidade(String strNuUmidade) {
		this.strNuUmidade = strNuUmidade;
	}
	public String getStrNuChuva() {
		return strNuChuva;
	}
	public void setStrNuChuva(String strNuChuva) {
		this.strNuChuva = strNuChuva;
	}
	public String getStrNuLuz() {
		return strNuLuz;
	}
	public void setStrNuLuz(String strNuLuz) {
		this.strNuLuz = strNuLuz;
	}
	
	
}
